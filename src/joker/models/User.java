package joker.models;

public class User
{
	private int _id;
	private String _name;
	private String _phoneNum;
	private String _passWord;
	private float _balance;
	private int _key;
	private float _discount;
	public int get_id() 
	{
		return _id;
	}
	public void set_id(int _id) 
	{
		this._id = _id;
	}
	public String get_name() 
	{
		return _name;
	}
	public void set_name(String _name)
	{
		this._name = _name;
	}
	public String get_phoneNum() 
	{
		return _phoneNum;
	}
	public void set_phoneNum(String _phoneNum) 
	{
		this._phoneNum = _phoneNum;
	}
	public String get_passWord()
	{
		return _passWord;
	}
	public void set_passWord(String _passWord) 
	{
		this._passWord = _passWord;
	}
	public float get_balance() {
		return _balance;
	}
	public void set_balance(float _balance)
	{
		this._balance = _balance;
	}
	public int get_key() {
		return _key;
	}
	public void set_key(int _key)
	{
		this._key = _key;
	}
	public float get_discount() 
	{
		return _discount;
	}
	public void set_discount(float _discount) 
	{
		this._discount = _discount;
	}
	
	
}
